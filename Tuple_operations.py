t = (10, 20, 30, 20, 40)

print("Tuple:", t)

# Access by index
print("First element:", t[0])

# Count occurrences
print("Count of 20:", t.count(20))

# Find index of element
print("Index of 30:", t.index(30))

# Length of tuple
print("Length:", len(t))

# Slicing
print("Slice t[1:4]:", t[1:4])
